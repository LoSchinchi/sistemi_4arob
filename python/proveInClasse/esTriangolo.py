tr = ((1.5, 3.6), (-1.0, 0.0), (5.1, 4.3))

area = 0.5 * abs(tr[0][0] * tr[1][1] + 
                tr[0][1] * tr[2][0] + 
                tr[1][0] * tr[2][1] - 
                tr[2][0] * tr[1][1] - 
                tr[2][1] * tr[0][0] - 
                tr[1][0] * tr[0][1])
print(f"area: {area:.3f}")